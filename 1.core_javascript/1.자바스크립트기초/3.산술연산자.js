console.log('=============증감 연산자==============');
//증감 연산자
var x = 3;
x++; //4
++x; //5
x--; //4
--x; //3
console.log(`x : ${x}`);



console.log('=============전위 연산 , 후위 연산 ==============');
//전위 연산 , 후위 연산 
var n1 = 10;
var n2 = n1++;
console.log(`n1 : ${n1} , n2 : ${n2}`);

var n3 = 10;
var n4 = ++n3;
console.log(`n3 : ${n3} , n4 : ${n4}`);


