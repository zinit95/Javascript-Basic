console.log('===========나머지 연산 ============');
// 나머지 연산 
console.log(27 % 5); //2
// 뒤에 숫자가 더 크면 앞에 숫자가 그대로 나머지, 몫은 0
console.log(4 % 10);

// 안되는 코드 나눗셈 불가
console.log(5 % 0); //NaN
console.log(0 % 5); //몫과 나머지 전부 0



console.log('=============제곱 연산==============');
//제곱 연산
